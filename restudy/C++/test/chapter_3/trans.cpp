#define de(x) cerr<<#x << " " <<x <<" "
#define deb(x) cerr<<#x << " " <<x <<"\n";

using namespace std;
const int cc = 66;
int main() {
    int x = 67;
    char xx{ x };
    char c{ cc }; // 定义为 acsii 码所对应的 字符
    cout << c << endl;
    deb(xx);

    // intergral promotion consisting of // 5 kinds
    // bool ,char, unsigned char,signed char,short -> int


    cout << 6.0 * 3 / 4  << endl;
    cout << 15 % 4 << endl;
    
    return 0;
}